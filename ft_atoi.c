int		ft_atoi(const char *s)
{
	int nb;
	int n;

	n = 1;
	nb = 0;
	while (*s == '\f' || *s == '\t' || *s == ' '
			|| *s == '\v' || *s == '\r' || *s == '\n')
		s++;
	if (*s == '+' || *s == '-')
	{
		if (*s == '-')
			n = -1;
		s++;
	}
	while (*s >= 48 && *s <= 57)
	{
		nb = nb * 10 + *s - 48;
		s++;
	}
	return (nb * n);
}
