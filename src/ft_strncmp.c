#include "libft.h"

int		ft_strncmp(char *s1, char *s2, size_t n)
{
	unsigned int i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && i <= n)
		i++;
	if (s1[i] - s2[i] == 0)
		return (0);
	return (s1[i] - s2[i] < 0 ? -1 : 1);
}
