int		ft_atoi(char *s)
{
	int nb;
	int n;
	int i;

	i = 0;
	n = 1;
	while (s[i] == '\f' || s[i] == '\t' || s[i] == ' '
			|| s[i] == '\v' || s[i] == '\r' || s[i] == '\n')
		i++;
	if (s[i] == '+')
		i++;
	if (s[i] == '-')
	{
		n = -1;
		i++;
	}
	while (s[i] >= 48 && s[i] <= 57)
	{
		nb = nb * 10 + s[i] - 48;
		i++;
	}
	return (nb * n);
}
